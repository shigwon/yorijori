// 이미지 로드 실패 핸들러
const handleImageError = (event) => {
  console.error('❌ 이미지 로드 실패:', event.target.src)
  error.value = '이미지를 불러올 수 없습니다.'
  isLoading.value = false
  isConnected.value = false
