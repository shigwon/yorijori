// 카카오맵 초기화 함수
const initDeliveryMap = () => {
  console.log('카카오맵 초기화 시작')
  
  if (!window.kakao || !window.kakao.maps) {
    console.error('카카오맵 API가 로드되지 않음')
    return
  }
  
  const container = document.getElementById('delivery-map')
